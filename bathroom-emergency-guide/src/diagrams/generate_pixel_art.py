def hex_to_rgba(h, alpha=255):
    """Convert hex string to RGBA tuple."""
    if h is None:
        return (0, 0, 0, 0)
    h = h.lstrip('#')
    return (int(h[:2], 16), int(h[2:4], 16), int(h[4:6], 16), alpha)


def render_map(pmap, palette, w, h):
    """Render a pixel-art sprite from a character map + palette."""
    img = Image.new('RGBA', (w, h), (0, 0, 0, 0))
    px = img.load()
    for y, row in enumerate(pmap):
        for x, ch in enumerate(row):
            c = palette.get(ch)
            if c is not None:
                px[x, y] = hex_to_rgba(c)
    return img


def save_sprite(img, name):
    """Save sprite and report."""
    path = os.path.join(OUT, name)
    img.save(path, 'PNG')
    print(f"  [OK] {path}")


